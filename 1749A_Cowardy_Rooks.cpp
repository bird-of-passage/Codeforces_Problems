#include<bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    
	int t; cin >> t;
	while (t--) {
		int n, m;
		cin >> n >> m;
		for (int i = 0; i < m; ++i) {
			int k; cin >> k >> k;
		}
		if (m < n) {
			cout << "YES\n";
		} else {
			cout << "NO\n";
		}
	}

    return 0;
}


